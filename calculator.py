def add(a,b):
    answer = a + b
    return answer

def substract(a,b):
    answer = a - b
    return answer

def multiply(a,b):
    answer = a * b
    return answer

def divide(a,b):
    answer = a / b
    return answer

def exp(a,b):
    answer = a ** b
    return answer

print(add(3,4)) #7
print(substract(5,2)) #3
print(multiply(7,8)) #56
print(divide(9,3)) #3
print(exp(2,2)) #4